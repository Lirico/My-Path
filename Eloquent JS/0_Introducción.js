/* 
    ¿Qué es la programación?

    La programación es el acto de construir un programa—un conjunto de instrucciones precisas que le dicen a una 
    computadora qué hacer. Porque las computadoras son bestias tontas y pedantes, la programación es fundamentalmente 
    tediosa y frustrante.

    Te permite hacer en segundos cosas que tardarían para siempre a mano. Es una forma de hacer que tu herramienta 
    computadora haga cosas que antes no podía. Ademas proporciona de un maravilloso ejercicio en pensamiento abstracto.
*/

/* 
    Lenguaje de programación

    La mayoría de la programación se realiza con lenguajes de programación. Un lenguaje de programación es un 
    lenguaje artificialmente construido que se utiliza para instruir ordenadores.
*/

/* 
    ¿Qué es un programa?

    Un programa son muchas cosas. Es una pieza de texto escrita por un programador, es la fuerza directriz que hace 
    que la computadora haga lo que hace, son datos en la memoria de la computadora, y sin embargo controla las 
    acciones realizadas en esta misma memoria. 

    Una analogía que es superficialmente adecuada es el de una máquina—muchas partes separadas tienden a estar 
    involucradas, y para hacer que todo funcione, tenemos que considerar la formas en las que estas partes se 
    interconectan y contribuyen a la operación de un todo.

    Un programa es un edificio de pensamiento. No cuesta nada construirlo, no pesa nada, y crece fácilmente bajo 
    nuestras manos que teclean.
*/

/* 
    ¿Qué es una computadora?

    Una computadora es una máquina física que actúa como un anfitrión para estas máquinas inmateriales. 
    Las computadoras en si mismas solo pueden hacer cosas estúpidamente sencillas. La razón por la que son 
    tan útiles es que hacen estas cosas a una velocidad increíblemente alta. Un programa puede ingeniosamente 
    combinar una cantidad enorme de estas acciones simples para realizar cosas bastante complicadas.
*/

/*
    ¿Qué es el código?

    Código es el texto que compone los programas. reo que leer código y escribir código son partes indispensables 
    del aprendizaje para programar.
*/